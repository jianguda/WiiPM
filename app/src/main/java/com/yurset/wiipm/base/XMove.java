package com.yurset.wiipm.base;

public class XMove {
	private Integer id;
	private Short index;
	private String name;
	private String type;
	private String cate;
	private Short power;
	private Short accur;
	private Short pp;
	private Short pri;
	private String info;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Short getIndex() {
		return index;
	}

	public void setIndex(Short index) {
		this.index = index;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getCate() {
		return cate;
	}

	public void setCate(String cate) {
		this.cate = cate;
	}

	public Short getPower() {
		return power;
	}

	public void setPower(Short power) {
		this.power = power;
	}

	public Short getAccur() {
		return accur;
	}

	public void setAccur(Short accur) {
		this.accur = accur;
	}

	public Short getPP() {
		return pp;
	}

	public void setPP(Short pp) {
		this.pp = pp;
	}

	public Short getPri() {
		return pri;
	}

	public void setPri(Short pri) {
		this.pri = pri;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}
}
